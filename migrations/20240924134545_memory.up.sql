  EC REAL,
  EU REAL,
  OC REAL,
  OU REAL,
  MC REAL,
  MU REAL,
  CCSC REAL,
  CCSU REAL,
  YGC REAL,
  YGCT REAL,
  FGC REAL,
  FGCT REAL,
  CGC REAL,
  CGCT REAL,
  GCT REAL,
  EXE_TIME TIMESTAMP
);
